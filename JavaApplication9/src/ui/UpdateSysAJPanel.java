    private void tblSysAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSysAMouseClicked
        // TODO add your handling code here:
        int selectedRowIndex = tblSysA.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a System Admin profile to Update");
            return;
        }
        DefaultTableModel model= (DefaultTableModel) tblSysA.getModel();
        SystemAdmin selectedSysAdmin = (SystemAdmin) model.getValueAt(selectedRowIndex, 0);
        txtSysAID.setText(String.valueOf(selectedSysAdmin.getSysID()));
        txtSysAName.setText(String.valueOf(selectedSysAdmin.getSysName()));
        txtSysAUname.setText(String.valueOf(selectedSysAdmin.getSysUsername()));
        txtSysAPswd.setText(String.valueOf(selectedSysAdmin.getSysPassword()));
    }//GEN-LAST:event_tblSysAMouseClicked

