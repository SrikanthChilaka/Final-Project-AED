    private void btnUpdSysAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdSysAActionPerformed
        // TODO add your handling code here:

        if(sAdmin!=null){
            DefaultTableModel model= (DefaultTableModel) tblSysA.getModel();
            if(tblSysA.getSelectedRowCount() == 1){
                if(isEmpty(txtSysAID.getText())) {
                    JOptionPane.showMessageDialog(this, "Please enter System Admin ID");
                    return;
                }
                String sysId = txtSysAID.getText();
                if(isEmpty(txtSysAName.getText())) {
                    JOptionPane.showMessageDialog(this, "Please enter System Admin Name");
                    return;
                }
                String sysN = txtSysAName.getText();
                if(isEmpty(txtSysAUname.getText())) {
                    JOptionPane.showMessageDialog(this, "Please enter System Admin Username");
                    return;
                }
                String sysUname = txtSysAUname.getText();
                if(isEmpty(txtSysAPswd.getText())) {
                    JOptionPane.showMessageDialog(this, "Please enter System Admin Password");
                    return;
                }
                String sysPswd = txtSysAPswd.getText();
                int selectedRowIndex = tblSysA.getSelectedRow();

                SystemAdmin sysA = sAdmin.getSAdmin().get(selectedRowIndex);
                sysA.setSysID(sysId);
                sysA.setSysName(sysN);
                sysA.setSysUsername(sysUname);
                sysA.setSysPassword(sysPswd);
            }

            JOptionPane.showMessageDialog(this, "The record has been updated successfully");
            populateTable();
        }

        else{
            JOptionPane.showMessageDialog(this,"Row empty.");
        }
    }//GEN-LAST:event_btnUpdSysAActionPerformed
    private void populateTable() {
        DefaultTableModel model= (DefaultTableModel) tblSysA.getModel();
        model.setRowCount(0);
        
        for(SystemAdmin sa : sAdmin.getSAdmin()){
            Object[] row = new Object[4];
            row[0] = sa;
            row[1] = sa.getSysName();
            row[2] = sa.getSysUsername();
            row[3] = sa.getSysPassword();
            model.addRow(row);
        }
    }

