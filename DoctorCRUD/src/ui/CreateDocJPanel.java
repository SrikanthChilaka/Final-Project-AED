    private void btnSaveDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveDocActionPerformed
        // TODO add your handling code here:
        if(doctr!=null){

            if(isEmpty(txtDocName.getText())) {
                JOptionPane.showMessageDialog(this, "Please enter Doctor Name");
                return;
            }
            String docName = txtDocName.getText();

            if(isEmpty(txtDocID.getText())) {
                JOptionPane.showMessageDialog(this, "Please enter Doctor ID");
                return;
            }
            String docID = txtDocID.getText();

            if(isEmpty(txtHospID.getText())) {
                JOptionPane.showMessageDialog(this, "Please enter Hospital ID");
                return;
            }
            String hospID = txtHospID.getText();

            if(isEmpty(txtDocUN.getText())) {
                JOptionPane.showMessageDialog(this, "Please enter Doc Username");
                return;
            }
            String docUN = txtDocUN.getText();

            if(isEmpty(txtDocPass.getText())) {
                JOptionPane.showMessageDialog(this, "Please enter Hospital ID Doc Password");
                return;
            }
            String docPass = txtDocPass.getText();

            Doctor doc = doctr.addNewDoc();

            doc.setDocName(docName);
            doc.setDocID(docID);
            doc.setHospID(hospID);
            doc.setDocUsername(docUN);
            doc.setDocPass(docPass);
            JOptionPane.showMessageDialog(this,"New Doctor Added to List");

            txtDocName.setText("");
            txtDocID.setText("");
            txtHospID.setText("");
            txtDocUN.setText("");
            txtHospID.setText("");

        }
        else{
            JOptionPane.showMessageDialog(this,"No Doctor");
        }
    }//GEN-LAST:event_btnSaveDocActionPerformed


